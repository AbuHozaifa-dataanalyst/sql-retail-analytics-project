/*
Business Question:
Which stores generate the highest total revenue?
*/

SELECT 
	st.Store_Name,
	SUM(s.Total_Amount) AS Total_Revenue
FROM Sales s
JOIN Stores st
	ON s.Store_ID = st.Store_ID
GROUP BY st.Store_Name
ORDER BY Total_Revenue DESC;



/*
Business Question:
What is the total revenue generated by each store?
*/

SELECT 
	st.Store_ID,
	st.Store_Name,
SUM(s.Total_Amount) AS Store_Revenue
FROM Sales s
JOIN Stores st
	ON s.Store_ID = st.Store_ID
GROUP BY st.Store_ID, st.Store_Name
ORDER BY Store_Revenue DESC;



/*
Business Question:
How many transactions does each store handle?
*/

SELECT 
	st.Store_Name,
	COUNT(s.Sale_ID) AS Total_Transactions
FROM Sales s
JOIN Stores st
	ON s.Store_ID = st.Store_ID
GROUP BY st.Store_Name
ORDER BY Total_Transactions DESC;



/*
Business Question:
What is the average transaction value for each store?
*/

SELECT 
	st.Store_Name,
	AVG(s.Total_Amount) AS Avg_Transaction_Value
FROM Sales s
JOIN Stores st
	ON s.Store_ID = st.Store_ID
GROUP BY st.Store_Name
ORDER BY Avg_Transaction_Value DESC;



