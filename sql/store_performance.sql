/*
Business Question:
Which stores sell the most units?
*/

SELECT 
	st.Store_Name,
SUM(s.Quantity) AS Total_Units_Sold
FROM Sales s
JOIN Stores st
	ON s.Store_ID = st.Store_ID
GROUP BY st.Store_Name
ORDER BY Total_Units_Sold DESC;



/*
Business Question:
Rank stores based on total revenue.
*/

SELECT 
	st.Store_Name,
	SUM(s.Total_Amount) AS Revenue,
	RANK() OVER (ORDER BY SUM(s.Total_Amount) DESC) AS Store_Rank
FROM Sales s
JOIN Stores st
	ON s.Store_ID = st.Store_ID
GROUP BY st.Store_Name;



/*
Business Question:
Which stores perform best within each region?
*/

SELECT 
	st.Region,
	st.Store_Name,
	SUM(s.Total_Amount) AS Revenue,
	RANK() OVER(
		PARTITION BY st.Region
		ORDER BY SUM(s.Total_Amount) DESC
	) AS Regional_Rank
FROM Sales s
JOIN Stores st
	ON s.Store_ID = st.Store_ID
GROUP BY st.Region, st.Store_Name;
